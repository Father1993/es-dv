                            в патрон светильника. Двуцокольные МГЛ популярны
                            благодаря возможности снижать потерю световой
                            энергии. Область применения напрямую зависит от
                            типов цоколей МГЛ, среди которых выделяют
                            одноцокольные МГЛ с керамической / кварцевой
                            горелкой, двухцокольные МГЛ с керамической /
                            кварцевой горелкой, бесцокольные МГЛ с кварцевой
                            горелкой.
                        </p>

                        <p><strong>Световой поток</strong></p>

                        <p>
                            Световой поток металлогалогенных ламп очень важен
                            при определении силы света лампа. Эта техническая
                            характеристика лампы способна раскрыть возможность
                            определенного источника света при освещении
                            помещения. Световая величина МГЛ составляет 75 ‑ 100
                            лм / Вт и превышает показатели других световых
                            источников. Так, вольфрамовая лампа накаливания
                            имеет всего лишь светоотдачу в 10 ‑ 22 лм / Вт.
                        </p>

                        <p><strong>Схема включения МГЛ</strong></p>

                        <p>
                            Схема включения металлогалогенной лампы сходна со
                            схемой всех газоразрядных ламп. Небольшое отличие
                            состоит лишь в том, что вместе с электромагнитным
                            или электронным ПРА, о которых читайте здесь,
                            требуется специальное поджигающее устройство,
                            которое обеспечивает зажигание в несколько кВт.
                            Подключение металлогалогенных ламп идет с балластом,
                            который создаёт сдвиг между током и напряжением, и
                            конденсатором, служащим для компенсации коэффициента
                            мощности. МГЛ поглощают малочастотный ток, а
                            электронные аппараты включения иногда гораздо легче
                            (в 3 ‑ 4 раза), так как функционируют как балласт,
                            зажигающее устройство и компенсирующий конденсатор.
                        </p>

