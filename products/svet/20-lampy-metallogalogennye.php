                        <p><strong>Срок службы</strong></p>
                        <p>
                            Срок действия немногих видов МГЛ может составлять 15
                            000 часов. Чтобы определить средний срок службы МГЛ
                            рекомендуется учитывать продолжительность
                            эксплуатации и их техническое устройство (дросселя
                            или электронный ПРА). Средняя частота включения и
                            ритм выключения ‑ еще один немаловажный признак,
                            влияющий на срок службы МГЛ. Длительность службы
                            таких ламп зависит от постоянной номинальной
                            мощности и избегания выключения МГЛ во время
                            запуска. Не рекомендуется использовать МГЛ, срок
                            эксплуатации которых превышает хотя бы 25%
                            указанного срока службы из-за возможности
                            растрескивания. По истечении срока службы у таких
                            ламп может снизиться уровень качества светового
                            потока.
                        </p>

                        <p><strong>Качество цветопередачи</strong></p>
                        <p>
                            При выборе ламп для освещения различных предметов и
                            сооружений нужно принимать во внимание её
                            способность к передаче истинного цвета и учитывать
                            возможные эффекты оттенков света. Это определяется
                            параметром индекса цветопередачи, о котором читайте
                            тут. Изначально МГЛ использовались для создания
                            света, максимально приближенного к естественному,
                            так как способны были излучать белый дневной свет с
                            индексом передачи 80. Современные МГЛ уже имеют
                            индекс цветопередачи свыше 90. Например, индекс
                            цветопередачи более 80 или 90 играет главенствующую
                            роль для придания естественного цвета продуктам.
                            Неестественный оттенок, который создается при
                            освещении ламп с низким индексом цветопередачи,
                            приводит к тому, что покупатель не обращает внимания
                            на товар или, более того, избегает его покупки.
                            Однако определить цветовые коэффициенты МГЛ 100% не
                            всегда возможно по причине фабричных отклонений или
                            без преодоления порога горения в 100 часов. Мощность
                            питания электрической сети также сказывается на
                            цветопередаче лампы. Недостаточная мощность питания
                            изменяет физическую температуру, так что свет такой
                            лампы приобретает синеватый оттенок. Качество
                            цветопередачи часто изменяется по мере эксплуатации,
                            отражаясь на свете лампы.
                        </p>

                        <p><strong>Цветовая температура</strong></p>
                        <p>
                            Характеристики цветовой температуры и спектральный
                            состав излучения, измеряемой в единицах Кельвина
                            (К.), очень важны для создания теплых или холодных
                            оттенков при освещении предметов и создания
                            правильного визуального образа. Так, способность МГЛ
                            создавать температуру горения со спектром от 2500
                            единиц Кельвина (приобретает жёлтый оттенок) до 20
                            000 единиц Кельвина (становится синим) может быть
                            вызвана необходимостью различного применения,
                            например, для растений или животных. Некоторые МГЛ
                            обладают функцией «предварительный прогрев»
                            (примерно 300 единиц Кельвина), что сказывается на
                            цветопередаче, но МГЛ нового поколения улучшили
                            показания от 100 до 200 единицах Кельвина.
                        </p>

                        <p><strong>Цоколь</strong></p>

                        <p>
                            Наиболее употребительными МГЛ считаются лампы с
