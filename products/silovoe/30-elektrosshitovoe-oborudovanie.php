                        переключает питание с основного источника на
                        дополнительный (генератор), в случае аварии
                        основного поставщика электроэнергии. После
                        устранения аварии, АВР перейдет с генератора на
                        основную линию и через несколько минут генератор
                        будет остановлен. Используется в производственных,
                        торговых, коммунальных зданиях, а также в коттеджах.
                    </p>

