                    <p>
                        <strong>Вводное распределительное устройство
                            (ВРУ)</strong>
                    </p>

                    <p>
                        Устройство, в которое входит комплекс
                        электротехнической автоматики и конструкций,
                        используется для приема вводного силового кабеля,
                        распределения питающих линий для ЩЭ, ЩК, ЩО, ВРУ,
                        учета электроэнергии, защиты линий от перегрузок и
                        короткого замыкания. Устанавливается на вводе жилых,
                        общественных зданий, а также в производственных
                        помещениях (цехах).
                    </p>

                    <hr />

                    <p>
                        <strong>Аварийный ввод резерва (АВР)</strong>
                    </p>

                    <p>
