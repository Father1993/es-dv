                    <p>
                        <strong>Щит освещения (ОЩ)</strong>
                    </p>

                    <p>
                        Щиты освещения устанавливают в административных,
                        торговых и офисных помещениях, для нечастых
                        оперативных включений и выключений автоматики. ЩО
                        защищает отходящие линии от перегрузок и коротких
                        замыканий.
                    </p>

                    <p>Щиты освещения подразделяется:</p>
