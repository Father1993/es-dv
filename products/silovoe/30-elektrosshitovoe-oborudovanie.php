                        </li>
                        <li>Отсек учета (электрические счетчики).</li>
                        <li>
                            Абонентский отсек (телефон, домофон, TV, радио и
                            пр.)
                        </li>
                    </ul>

                    <hr />

                    <p>
                        <strong>Щит квартирный (ЩК)</strong>
                    </p>

                    <p>
                        Как правило, располагается на вводе в квартиру в
                        районе прихожей. Основное назначение ЩК – это учет
